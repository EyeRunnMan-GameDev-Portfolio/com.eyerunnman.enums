        /// wednesday , third day of week
        /// </summary>
        Wednesday = 2,
        /// <summary>
        /// thursday , fourth day of week
        /// </summary>
        Thursday = 3,
        /// <summary>
        /// friday , fifth day of week
        /// </summary>
        Friday = 4,
